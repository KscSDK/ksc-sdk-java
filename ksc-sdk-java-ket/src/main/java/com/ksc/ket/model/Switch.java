package com.ksc.ket.model;

public class Switch {
	private int sn;
	private int sm;
	private int hv;
	private int mv;
	private int un;
	private int od;
	private int bu;

	public int getSn() {
		return sn;
	}

	public void setSn(int sn) {
		this.sn = sn;
	}

	public int getSm() {
		return sm;
	}

	public void setSm(int sm) {
		this.sm = sm;
	}

	public int getHv() {
		return hv;
	}

	public void setHv(int hv) {
		this.hv = hv;
	}

	public int getMv() {
		return mv;
	}

	public void setMv(int mv) {
		this.mv = mv;
	}

	public int getUn() {
		return un;
	}

	public void setUn(int un) {
		this.un = un;
	}

	public int getOd() {
		return od;
	}

	public void setOd(int od) {
		this.od = od;
	}

	public int getBu() {
		return bu;
	}

	public void setBu(int bu) {
		this.bu = bu;
	}

}
