package com.ksc.offline.model;

public class Video {
	private String vr;
	private String vb;
	private String vcodec;
	private int width;
	private int height;
	private int as;
	private String rotate;
	private int vn;
	private int shortSide;
	private int orientationAdapt;
	private int gop;

	public String getVr() {
		return vr;
	}

	public void setVr(String vr) {
		this.vr = vr;
	}

	public String getVb() {
		return vb;
	}

	public void setVb(String vb) {
		this.vb = vb;
	}

	public String getVcodec() {
		return vcodec;
	}

	public void setVcodec(String vcodec) {
		this.vcodec = vcodec;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getAs() {
		return as;
	}

	public void setAs(int as) {
		this.as = as;
	}

	public String getRotate() {
		return rotate;
	}

	public void setRotate(String rotate) {
		this.rotate = rotate;
	}

	public int getVn() {
		return vn;
	}

	public void setVn(int vn) {
		this.vn = vn;
	}

	public int getShortSide() {
		return shortSide;
	}

	public void setShortSide(int shortSide) {
		this.shortSide = shortSide;
	}

	public int getOrientationAdapt() {
		return orientationAdapt;
	}

	public void setOrientationAdapt(int orientationAdapt) {
		this.orientationAdapt = orientationAdapt;
	}

	public int getGop() {
		return gop;
	}

	public void setGop(int gop) {
		this.gop = gop;
	}

}
