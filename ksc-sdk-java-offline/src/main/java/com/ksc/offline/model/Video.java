package com.ksc.offline.model;

public class Video {
	private String vr;
	private String vb;
	private String vcodec;
	private int width;
	private int height;
	private int as;
	private String rotate;
	private int vn;
	public String getVr() {
		return vr;
	}
	public void setVr(String vr) {
		this.vr = vr;
	}
	public String getVb() {
		return vb;
	}
	public void setVb(String vb) {
		this.vb = vb;
	}
	public String getVcodec() {
		return vcodec;
	}
	public void setVcodec(String vcodec) {
		this.vcodec = vcodec;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getAs() {
		return as;
	}
	public void setAs(int as) {
		this.as = as;
	}
	public String getRotate() {
		return rotate;
	}
	public void setRotate(String rotate) {
		this.rotate = rotate;
	}
	public int getVn() {
		return vn;
	}
	public void setVn(int vn) {
		this.vn = vn;
	}
	
	
}
