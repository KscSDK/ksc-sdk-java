package com.ksc.offline.model;

public class Audio {
	private String ar;
	private String ab;
	private String acodec;
	private int an;
	public String getAr() {
		return ar;
	}
	public void setAr(String ar) {
		this.ar = ar;
	}
	public String getAb() {
		return ab;
	}
	public void setAb(String ab) {
		this.ab = ab;
	}
	public String getAcodec() {
		return acodec;
	}
	public void setAcodec(String acodec) {
		this.acodec = acodec;
	}
	public int getAn() {
		return an;
	}
	public void setAn(int an) {
		this.an = an;
	}
	
}
